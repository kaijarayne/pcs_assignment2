var output = "";

for (var i = 10; i >= 0 ; i -= 1) {
	switch (myName[i]) {
		case "a":
		case "e":
		case "i":
		case "o":
		case "u":
		break;
		default: 

		output = output + myName[i]

	}
}

console.log(output);


